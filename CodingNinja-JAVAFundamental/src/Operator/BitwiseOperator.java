package Operator;

public class BitwiseOperator {
	public static void main(String[] args) {
		System.out.println(2^3);
		System.out.println(2|3);
		System.out.println(2&3);
		System.out.println(~2);
		
		
		System.out.println(30<<2);
		System.out.println(30<<1);
		System.out.println(-20>>2);
		System.out.println(-20<<1);
	}

}
