package Operator;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a=10;
		int c=a++;
		System.out.println(c);
		a--;
		System.out.println(a);
		
		int b=10;
		int d=++b;
		System.out.println(d);
		
		int k=4;
		k *=4;
		System.out.println(k);
		

	}

}
